import { defineProblem } from "../../core/schemas";

export default defineProblem({
  id: "learn-comparison",
  stage: "learn",
  scenario: "basic",
  copy: {
    title: "比較演算子を知る",
    prompt: `## 大小・等しいを調べる

比較の結果は必ず **\`true\` か \`false\`**（真偽値）になる。

| 記号 | 意味 |
|---|---|
| \`>\` \`>=\` | より大きい / 以上 |
| \`<\` \`<=\` | より小さい / 以下 |
| \`===\` | 等しい（型も値も） |
| \`!==\` | 等しくない |

## \`==\` ではなく \`===\` を使う

\`===\` は**型まで**見て比較する。\`==\` は型を勝手に変換するので予想外の結果になりやすい。

\`\`\`ts
1 === "1"  // false（数値と文字列で型が違う）
1 == "1"   // true （== は型を変換してしまう・非推奨）
\`\`\`

迷ったら \`===\` / \`!==\` を使えばよい。`,
    hints: [
      "比較の結果は true / false になる。",
      '`===` は型も比較する。数値の 1 と文字列の "1" は別物。',
    ],
  },
  learn: {
    quiz: [
      {
        prompt: '`1 === "1"` の結果は？',
        snippet: `1 === "1"`,
        choices: ["false", "true"],
        answer: "false",
        explain: '`===` は型も比較する。数値の 1 と文字列の "1" は型が違うので false。',
      },
      {
        prompt: "`2 !== 3` の結果は？",
        choices: ["true", "false"],
        answer: "true",
        explain: "`!==` は「等しくない」。2 と 3 は違うので true。",
      },
      {
        prompt: "`==` と `===` の違いとして正しいのは？",
        choices: ["=== は型も比較する", "== の方が新しくて安全", "意味は全く同じ"],
        answer: "=== は型も比較する",
        explain: "`===` は型も値も一致を要求。`==` は型を変換するので予想外になりやすく非推奨。",
      },
    ],
  },
});

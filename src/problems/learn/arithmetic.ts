import { defineProblem } from "../../core/schemas";

export default defineProblem({
  id: "learn-arithmetic",
  stage: "learn",
  scenario: "basic",
  copy: {
    title: "四則演算を知る",
    prompt: `## 計算の記号

数値どうしの計算はこの記号で書く。

| 記号 | 意味 | 例 |
|---|---|---|
| \`+\` | 足す | \`2 + 3\` → 5 |
| \`-\` | 引く | \`5 - 2\` → 3 |
| \`*\` | かける | \`4 * 3\` → 12 |
| \`/\` | わる | \`10 / 4\` → 2.5 |
| \`%\` | 余り | \`7 % 3\` → 1 |

## 計算の順番（優先順位）

算数と同じで、**\`*\` \`/\` \`%\` は \`+\` \`-\` より先**に計算される。

\`\`\`ts
2 + 3 * 4   // → 14（先に 3 * 4 = 12、そのあと + 2）
\`\`\`

\`( )\` で囲むと、**そこが最優先**になる。順番を変えたいときに使う。

\`\`\`ts
(2 + 3) * 4 // → 20（先に 2 + 3 = 5、そのあと * 4）
\`\`\`

## 2つの落とし穴

- \`/\` は**小数で返る**。\`10 / 4\` は \`2\` ではなく \`2.5\`。
- \`+\` は片方が**文字列だと連結**になる。\`"5" + 3\` は \`8\` ではなく \`"53"\`。`,
    hints: [
      "`%` は「割った余り」。`7 % 3` は 7 ÷ 3 の余りで 1。",
      "文字列 + 数値 は文字列としてつながる。",
      "`* / %` は `+ -` より先。順番を変えたいときは ( ) で囲む。",
    ],
  },
  learn: {
    quiz: [
      {
        prompt: "`7 % 3` の結果は？",
        choices: ["1", "2", "0"],
        answer: "1",
        explain: "`%` は余り。7 ÷ 3 = 2 あまり 1 なので結果は 1。",
      },
      {
        prompt: "`10 / 4` の結果は？",
        choices: ["2.5", "2", "3"],
        answer: "2.5",
        explain: "JS の `/` は小数で返る。2 や切り捨てにはならず 2.5。",
      },
      {
        prompt: '`"5" + 3` の結果は？',
        snippet: `"5" + 3`,
        choices: ['"53"', "8", '"8"'],
        answer: '"53"',
        explain: "片方が文字列だと `+` は連結になる。`\"5\"` と `3` がつながって `\"53\"`。",
      },
      {
        prompt: "`2 + 3 * 4` の結果は？",
        snippet: `2 + 3 * 4`,
        choices: ["14", "20", "24"],
        answer: "14",
        explain: "`*` は `+` より先。3 * 4 = 12 を先に計算して、+ 2 で 14。",
      },
      {
        prompt: "`(2 + 3) * 4` の結果は？",
        snippet: `(2 + 3) * 4`,
        choices: ["20", "14", "9"],
        answer: "20",
        explain: "( ) が最優先。2 + 3 = 5 を先に計算して、* 4 で 20。",
      },
    ],
  },
});
